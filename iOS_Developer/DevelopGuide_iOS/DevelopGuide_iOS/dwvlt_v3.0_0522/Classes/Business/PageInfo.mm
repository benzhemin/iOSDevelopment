#import "PageInfo.h"

PageInfo::PageInfo()
: m_pageObject(XdwNull)
, m_pageWidth(-1)
, m_pageHeight(-1)
, m_pageIndex(-1)
{
	m_PageOrigin.x = 0;
	m_PageOrigin.y = 0;
}

long PageInfo::GetWidth(PAGE_MODE mode)
{
	return m_pageWidth;
}

long PageInfo::GetHeight(PAGE_MODE mode)
{
	return m_pageHeight;
}

MP_Pos PageInfo::GetPagePos(PAGE_MODE mode)
{
	return m_PageOrigin;
}
